        $data = [];
        foreach ($comments as $comment) {
            $data[] = [
                'id' => $comment->id,
                'comment' => $comment->comment,
                'course' => isset($comment->course) ? $comment->course->name : "",
                'user' => isset($comment->user) ? $comment->user->name : "",


            ];
        }
        // dd($data);

        return view('admin.commentsTable.show',['data'=>$data]);
    }

